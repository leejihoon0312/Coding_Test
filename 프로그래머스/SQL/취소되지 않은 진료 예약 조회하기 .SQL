SELECT APNT_NO,	PT_NAME,	PATIENT.PT_NO,	DOCTOR.MCDP_CD,	DR_NAME,	APNT_YMD

FROM

(SELECT *
from APPOINTMENT
where MCDP_CD = "cs" and 	APNT_CNCL_YN = "n" and date_format(APNT_YMD,"%Y-%m-%d") = "2022-04-13") AS APPOINTMENT

JOIN

(SELECT *
 FROM DOCTOR
) AS DOCTOR

ON APPOINTMENT.MDDR_ID = DOCTOR.DR_ID

JOIN

(SELECT *
 FROM PATIENT
) AS PATIENT

ON PATIENT.PT_NO = APPOINTMENT.PT_NO

ORDER BY APNT_YMD